package cn.meng;

public enum TestEnum {

    A(0,"a"),
    B(0,"b");
    private Integer type;
    private String name;

    TestEnum(Integer type, String name) {
        this.type = type;
        this.name = name;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
